import { useMemo, useState } from 'react'

import { calculateMortgagePlan } from './lib/mortgage'

const euroFormatter = new Intl.NumberFormat('it-IT', {
  style: 'currency',
  currency: 'EUR',
  maximumFractionDigits: 2,
})

const pdfCurrencyFormatter = new Intl.NumberFormat('en-US', {
  style: 'currency',
  currency: 'EUR',
  maximumFractionDigits: 2,
})

const PAGE_LINE_LIMIT = 42

const toPdfSafeText = (value: string) =>
  value
    .replace(/\\/g, '\\\\')
    .replace(/\(/g, '\\(')
    .replace(/\)/g, '\\)')
    .replace(/[^\x20-\x7e]/g, '?')

const formatCurrencyForPdf = (amount: number) =>
  pdfCurrencyFormatter.format(amount).replace('€', 'EUR').replace(/\u00a0/g, ' ')

const PDF_COLUMN_WIDTHS = {
  month: 6,
  payment: 14,
  principal: 14,
  interest: 14,
  bankCost: 14,
  remaining: 18,
} as const

const padPdfCell = (value: string, width: number, align: 'left' | 'right' = 'right') => {
  if (value.length >= width) {
    return value.slice(0, width)
  }

  return align === 'left' ? value.padEnd(width, ' ') : value.padStart(width, ' ')
}

const createPdfTableLine = (
  month: string,
  payment: string,
  principal: string,
  interest: string,
  bankCost: string,
  remainingCapital: string,
) =>
  [
    padPdfCell(month, PDF_COLUMN_WIDTHS.month, 'left'),
    padPdfCell(payment, PDF_COLUMN_WIDTHS.payment),
    padPdfCell(principal, PDF_COLUMN_WIDTHS.principal),
    padPdfCell(interest, PDF_COLUMN_WIDTHS.interest),
    padPdfCell(bankCost, PDF_COLUMN_WIDTHS.bankCost),
    padPdfCell(remainingCapital, PDF_COLUMN_WIDTHS.remaining),
  ].join(' | ')

function createPdfDocumentFromLines(lines: string[]) {
  const pages: string[][] = []
  for (let index = 0; index < lines.length; index += PAGE_LINE_LIMIT) {
    pages.push(lines.slice(index, index + PAGE_LINE_LIMIT))
  }

  const objects: Record<number, string> = {}
  const fontObjectId = 3
  const firstPageObjectId = 4

  objects[1] = '<< /Type /Catalog /Pages 2 0 R >>'
  objects[2] = `<< /Type /Pages /Kids [${pages.map((_, pageIndex) => `${firstPageObjectId + pageIndex * 2} 0 R`).join(' ')}] /Count ${pages.length} >>`
  objects[fontObjectId] = '<< /Type /Font /Subtype /Type1 /BaseFont /Courier >>'

  pages.forEach((pageLines, pageIndex) => {
    const pageObjectId = firstPageObjectId + pageIndex * 2
    const contentObjectId = pageObjectId + 1

    const textOperations = pageLines.map((line) => `(${toPdfSafeText(line)}) Tj T*`).join('\n')
    const stream = `BT
/F1 10 Tf
40 560 Td
14 TL
${textOperations}
ET`

    objects[pageObjectId] =
      `<< /Type /Page /Parent 2 0 R /MediaBox [0 0 842 595] /Resources << /Font << /F1 ${fontObjectId} 0 R >> >> /Contents ${contentObjectId} 0 R >>`
    objects[contentObjectId] = `<< /Length ${stream.length} >>\nstream\n${stream}\nendstream`
  })

  const orderedObjectIds = Object.keys(objects)
    .map(Number)
    .sort((first, second) => first - second)

  let documentContent = '%PDF-1.4\n'
  const offsets: number[] = [0]

  orderedObjectIds.forEach((objectId) => {
    offsets[objectId] = documentContent.length
    documentContent += `${objectId} 0 obj\n${objects[objectId]}\nendobj\n`
  })

  const xrefStart = documentContent.length
  documentContent += `xref\n0 ${orderedObjectIds.length + 1}\n`
  documentContent += '0000000000 65535 f \n'
  orderedObjectIds.forEach((objectId) => {
    documentContent += `${offsets[objectId].toString().padStart(10, '0')} 00000 n \n`
  })
  documentContent += `trailer\n<< /Size ${orderedObjectIds.length + 1} /Root 1 0 R >>\nstartxref\n${xrefStart}\n%%EOF`

  return new Blob([documentContent], { type: 'application/pdf' })
}

function App() {
  const [houseCost, setHouseCost] = useState(250000)
  const [downPayment, setDownPayment] = useState(50000)
  const [years, setYears] = useState(25)
  const [annualInterestRate, setAnnualInterestRate] = useState(0.03)
  const [monthlyBankCost, setMonthlyBankCost] = useState(0)
  const [showPlan, setShowPlan] = useState(false)

  const plan = useMemo(
    () =>
      calculateMortgagePlan({
        houseCost,
        downPayment,
        years,
        annualInterestRate,
        monthlyBankCost,
      }),
    [annualInterestRate, downPayment, houseCost, monthlyBankCost, years],
  )

  const handleExportPlanAsPdf = () => {
    const lines = [
      'Mortgage Plan',
      'Generated by Mortgage Calculator (Alla Francese)',
      '',
      `House cost: ${formatCurrencyForPdf(houseCost)}`,
      `Down payment: ${formatCurrencyForPdf(downPayment)}`,
      `Duration: ${years} years`,
      `Annual interest rate: ${(annualInterestRate * 100).toFixed(2)}%`,
      `Monthly bank cost: ${formatCurrencyForPdf(monthlyBankCost)}`,
      `Monthly payment: ${formatCurrencyForPdf(plan.monthlyPayment)}`,
      `Total capital paid: ${formatCurrencyForPdf(plan.totalCapitalPaid)}`,
      `Total interest paid: ${formatCurrencyForPdf(plan.totalInterest)}`,
      `Total capital + interest: ${formatCurrencyForPdf(plan.totalCapitalAndInterest)}`,
      `Total paid: ${formatCurrencyForPdf(plan.totalPaid)}`,
      '',
      createPdfTableLine('Month', 'Payment', 'Principal', 'Interest', 'Bank cost', 'Remaining capital'),
      '-'.repeat(91),
      ...plan.installments.map(
        (installment) =>
          createPdfTableLine(
            installment.month.toString(),
            formatCurrencyForPdf(installment.payment),
            formatCurrencyForPdf(installment.principal),
            formatCurrencyForPdf(installment.interest),
            formatCurrencyForPdf(installment.bankCost),
            formatCurrencyForPdf(installment.remainingPrincipal),
          ),
      ),
      '-'.repeat(91),
      createPdfTableLine(
        'TOTAL',
        formatCurrencyForPdf(plan.totalPaid),
        formatCurrencyForPdf(plan.totalCapitalPaid),
        formatCurrencyForPdf(plan.totalInterest),
        formatCurrencyForPdf(plan.totalBankCosts),
        formatCurrencyForPdf(0),
      ),
    ]

    const pdfBlob = createPdfDocumentFromLines(lines)
    const pdfDownloadUrl = URL.createObjectURL(pdfBlob)
    const linkElement = document.createElement('a')
    linkElement.href = pdfDownloadUrl
    linkElement.download = `mortgage-plan-${new Date().toISOString().slice(0, 10)}.pdf`
    linkElement.click()
    URL.revokeObjectURL(pdfDownloadUrl)
  }

  return (
    <main className="min-h-screen bg-slate-950 px-4 py-8 text-slate-100 sm:px-6 lg:px-8">
      <section className="mx-auto w-full max-w-5xl">
        <h1 className="text-2xl font-bold tracking-tight sm:text-3xl">Mortgage Calculator (Alla Francese)</h1>
        <p className="mt-2 text-sm text-slate-300 sm:text-base">
          Simulate your mortgage plan in euro. The monthly payment includes bank monthly costs.
        </p>

        <div className="mt-6 grid gap-6 lg:grid-cols-[320px_1fr]">
          <form className="space-y-4 rounded-xl border border-slate-800 bg-slate-900 p-4 sm:p-5">
            <InputField
              id="houseCost"
              label="House cost (€)"
              value={houseCost}
              min={0}
              step={1000}
              onValueChange={setHouseCost}
            />
            <InputField
              id="downPayment"
              label="Down payment (€)"
              value={downPayment}
              min={0}
              step={1000}
              onValueChange={setDownPayment}
            />
            <InputField
              id="years"
              label="Mortgage duration (years)"
              value={years}
              min={1}
              step={1}
              onValueChange={setYears}
            />
            <InputField
              id="annualInterestRate"
              label="Annual interest rate"
              value={annualInterestRate}
              min={0}
              max={1}
              step={0.001}
              onValueChange={setAnnualInterestRate}
              hint="Default is 0.03 (3%)"
            />
            <InputField
              id="monthlyBankCost"
              label="Monthly bank cost (€)"
              value={monthlyBankCost}
              min={0}
              step={1}
              onValueChange={setMonthlyBankCost}
            />
          </form>

          <div className="rounded-xl border border-slate-800 bg-slate-900 p-4 sm:p-5">
            <h2 className="text-lg font-semibold">Summary</h2>
            <dl className="mt-4 grid grid-cols-1 gap-3 text-sm sm:grid-cols-2">
              <SummaryItem label="Loan principal" value={euroFormatter.format(plan.principal)} />
              <SummaryItem label="Monthly payment" value={euroFormatter.format(plan.monthlyPayment)} />
              <SummaryItem
                label="Monthly rate (without bank cost)"
                value={euroFormatter.format(plan.monthlyRateWithoutBankCost)}
              />
              <SummaryItem label="Total capital paid" value={euroFormatter.format(plan.totalCapitalPaid)} />
              <SummaryItem label="Total interest" value={euroFormatter.format(plan.totalInterest)} />
              <SummaryItem
                label="Total capital + interest"
                value={euroFormatter.format(plan.totalCapitalAndInterest)}
              />
              <SummaryItem label="Total bank costs" value={euroFormatter.format(plan.totalBankCosts)} />
              <SummaryItem label="Total paid" value={euroFormatter.format(plan.totalPaid)} />
            </dl>

            <div className="mt-5 flex flex-wrap gap-3">
              <button
                type="button"
                onClick={() => setShowPlan((current) => !current)}
                className="rounded-md bg-emerald-500 px-4 py-2 text-sm font-semibold text-slate-900 transition hover:bg-emerald-400"
              >
                {showPlan ? 'Hide plan' : 'Show plan'}
              </button>
              <button
                type="button"
                onClick={handleExportPlanAsPdf}
                className="rounded-md border border-emerald-400 px-4 py-2 text-sm font-semibold text-emerald-300 transition hover:bg-emerald-500/10"
              >
                Export plan as PDF
              </button>
            </div>

            {showPlan ? (
              <ul className="mt-5 space-y-3">
                {plan.installments.map((installment) => (
                  <li
                    key={installment.month}
                    className="rounded-lg border border-slate-700 bg-slate-950/60 p-3 text-xs sm:text-sm"
                  >
                    <p className="font-semibold text-emerald-300">Month {installment.month}</p>
                    <div className="mt-2 grid grid-cols-2 gap-2 sm:grid-cols-4">
                      <PlanValue label="Payment" value={euroFormatter.format(installment.payment)} />
                      <PlanValue label="Principal" value={euroFormatter.format(installment.principal)} />
                      <PlanValue label="Interest" value={euroFormatter.format(installment.interest)} />
                      <PlanValue label="Bank cost" value={euroFormatter.format(installment.bankCost)} />
                      <PlanValue
                        label="Remaining capital"
                        value={euroFormatter.format(installment.remainingPrincipal)}
                      />
                    </div>
                  </li>
                ))}
              </ul>
            ) : null}

            {showPlan ? (
              <div className="mt-5 rounded-lg border border-emerald-500/30 bg-emerald-500/5 p-4 text-sm">
                <h3 className="text-base font-semibold text-emerald-200">Plan totals</h3>
                <div className="mt-3 grid grid-cols-1 gap-2 sm:grid-cols-3">
                  <PlanValue label="Capital paid" value={euroFormatter.format(plan.totalCapitalPaid)} />
                  <PlanValue label="Interest paid" value={euroFormatter.format(plan.totalInterest)} />
                  <PlanValue
                    label="Capital + interest"
                    value={euroFormatter.format(plan.totalCapitalAndInterest)}
                  />
                </div>
              </div>
            ) : null}
          </div>
        </div>
      </section>
    </main>
  )
}

type InputFieldProps = {
  id: string
  label: string
  value: number
  min?: number
  max?: number
  step?: number
  hint?: string
  onValueChange: (value: number) => void
}

function InputField({ id, label, value, min, max, step, hint, onValueChange }: InputFieldProps) {
  return (
    <label className="block" htmlFor={id}>
      <span className="text-sm font-medium text-slate-200">{label}</span>
      <input
        id={id}
        type="number"
        className="mt-1 w-full rounded-md border border-slate-700 bg-slate-950 px-3 py-2 text-slate-100 outline-none ring-emerald-400 focus:ring"
        value={value}
        min={min}
        max={max}
        step={step}
        onChange={(event) => onValueChange(Number(event.target.value))}
      />
      {hint ? <span className="mt-1 block text-xs text-slate-400">{hint}</span> : null}
    </label>
  )
}

function SummaryItem({ label, value }: { label: string; value: string }) {
  return (
    <div className="rounded-md border border-slate-700 bg-slate-950/60 p-3">
      <dt className="text-slate-400">{label}</dt>
      <dd className="mt-1 font-semibold text-slate-100">{value}</dd>
    </div>
  )
}

function PlanValue({ label, value }: { label: string; value: string }) {
  return (
    <p>
      <span className="text-slate-400">{label}: </span>
      <span className="font-medium text-slate-100">{value}</span>
    </p>
  )
}

export default App
